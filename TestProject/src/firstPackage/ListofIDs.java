package firstPackage;

public class ListofIDs {

	String name = "";
	int ID = 0;
	
	ListofIDs(String name, int ID){
		this.name = name;
		this.ID = ID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getID() {
		return ID;
	}

	public void setID(int iD) {
		ID = iD;
	}
	
	
	
	
}
